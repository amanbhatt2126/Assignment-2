
public class Q6 {

	public static void main(String[] args) {
		// Q6 Write a do-while loop that will print out all the multiples of 3 from 3 to 36, that
		// is: 3 6 9 12 15 18 21 24 27 30 33 36. 
		
		int n =0 ;
		do {
			n = n+3 ; 
			System.out.println(n);
		}while(n<=33) ;
		

	}

}
